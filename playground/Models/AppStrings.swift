// MARK: - Localized String Access

extension AppStrings {
    /// Get a localized string by key
    /// Usage: AppStrings.localized(.Home.title)
    static func localized(_ key: String) -> String {
        return LocalizationManager.shared.localizedString(for: key)
    }
    
    /// Get a localized string with comment
    static func localized(_ key: String, comment: String) -> String {
        return LocalizationManager.shared.localizedString(for: key, comment: comment)
    }
    
    /// Get a localized string with format arguments
    static func localized(_ key: String, arguments: CVarArg..., comment: String = "") -> String {
        let format = LocalizationManager.shared.localizedString(for: key, comment: comment)
        return String(format: format, arguments: arguments)
    }
}

